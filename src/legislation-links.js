const pinnedLaws = pinnedTitles.map((title) => laws.find((item) => item[0] === title));
const orderedLaws = [
  ...pinnedLaws,
  ...laws.filter((item) => !pinnedTitles.includes(item[0])).sort((a, b) => a[0].localeCompare(b[0], 'tr-TR')),
];
const list = document.querySelector('#legislation-list');
const count = document.querySelector('#legislation-count');
let legislationSearchTimer;
const officialUrl = ([, , , number, type]) => 'https://www.mevzuat.gov.tr/mevzuat?MevzuatNo=' + number + '&MevzuatTur=' + type + '&MevzuatTertip=5';
function render(query = '') {
  const visible = orderedLaws.filter(([title]) => normalize(title).includes(normalize(query)));
  count.textContent = visible.length + ' bağlantı';
  list.innerHTML = visible.map((item) => {
    const [title, date, issue] = item;
    const index = orderedLaws.indexOf(item);
    return '<a class="legislation-link" href="' + officialUrl(item) + '" target="_blank" rel="noopener noreferrer"><span class="legislation-number">' + String(index + 1).padStart(2, '0') + '</span><span class="legislation-copy"><span class="legislation-title">' + title + '</span><span class="legislation-meta">Resmî Gazete Tarihi: ' + date + ' · Sayısı: ' + issue + '</span></span><span class="legislation-arrow">↗</span></a>';
  }).join('');
}
document.querySelector('#legislation-filter').addEventListener('input', (event) => {
  clearTimeout(legislationSearchTimer);
  legislationSearchTimer = setTimeout(() => render(event.target.value), 80);
});
render();
